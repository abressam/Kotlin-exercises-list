// 12. Utilize um mapa para representar um dicionário de palavras e suas traduções.

fun main() {
    val dicionario = mapOf(
        "hello" to "olá",
        "apple" to "maçã",
        "car" to "carro",
        "computer" to "computador"
    )

    println("Tradução de 'hello': ${dicionario["hello"]}")
    println("Tradução de 'apple': ${dicionario["apple"]}")
    println("Tradução de 'car': ${dicionario["car"]}")
    println("Tradução de 'computer': ${dicionario["computer"]}")
}








